namespace tiglib\stats;

class chi2 {

    /**
        Returns an array containing 2 elements: the chi2 value and the p-value.
        @param  $df Degree of freedom
        @param  $O  Observed distribution
        @param  $E  Expected distribution
    **/
    public static function chi2AndProba(int $df, array $O, array $E) {
        $chi2 = self::chi2($O, $E);
        return [
            $chi2,
            self::chi2Proba($chi2, $df),
        ];
    }
    
    // in case: https://www.socscistatistics.com/pvalues/chidistribution/calculator/
    
    /**
        Computes the chi square value to compare observed and expected values,
        for contingency table with only one column.
        @param  $O  Array containing observed values (regular or associative)
        @param  $E  Array containing expected values (regular or associative)
    **/
    public static function chi2(array $O, array $E): int|float{
        $res = 0;
        $N = count($O);
        $keysE = array_keys($E);
        $keysO = array_keys($O);
        for($i=0; $i < $N; $i++){
            if($E[$keysE[$i]] != 0){
                $res += pow($O[$keysO[$i]] - $E[$keysE[$i]], 2) / $E[$keysE[$i]];
            }
        }
        return $res;
    }
    
